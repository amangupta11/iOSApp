#import "UIColor+BCLAppColors.h"

@implementation UIColor (BCLAppColors)

+ (UIColor *)greenAppColor
{
    return [UIColor colorWithRed:0.09 green:0.76 blue:0.77 alpha:1];
}

+ (UIColor *)blueAppColor
{
    return [UIColor colorWithRed:0.384 green:0.73 blue:0.91 alpha:1.0];
}

+ (UIColor *)redAppColor
{
    return [UIColor colorWithRed:0.96 green:0.2 blue:0.25 alpha:1];
}

@end
